    int vidSteps = 0;
    @Override
    public void reportGameStatus(IStateQWOP state, CommandQWOP command, int timestep) {
        if (command != null)
            System.out.println(command.keys);

        if (timestep == 0) {
            System.out.println("reset");
            try {
                grab.seekToFramePrecise(0);
                vidSteps = 0;
            } catch (IOException | JCodecException e) {
                e.printStackTrace();
            }
        }

        Picture picture = null;
            if (vidSteps < timestep / 30. * 24) {
            try {
                picture = grab.getNativeFrame();
            } catch (IOException e) {
                e.printStackTrace();
            }
            BufferedImage bufferedImage = AWTUtil.toBufferedImage(picture);
            BufferedImage imgArgb = new BufferedImage(bufferedImage.getWidth(), bufferedImage.getHeight(),
                    BufferedImage.TYPE_INT_ARGB);
            ColorConvertOp op = new ColorConvertOp(null);
            op.filter(bufferedImage, imgArgb);

            setAlpha(imgArgb, (byte) 128);
            float scale = 2.15f;
            img.setImage(scale(imgArgb, (int) (scale * imgArgb.getWidth()), (int) (scale * imgArgb.getHeight()) - 10));
            frame.repaint();
            vidSteps++;
        }
    }

