    public static void setAlpha(BufferedImage obj_img, byte alpha) {
        alpha %= 0xff;
        for (int cx=0;cx<obj_img.getWidth();cx++) {
            for (int cy=0;cy<obj_img.getHeight();cy++) {
                int color = obj_img.getRGB(cx, cy);

                int mc = (alpha << 24) | 0x00ffffff;
                int newcolor = color & mc;
                obj_img.setRGB(cx, cy, newcolor);
            }
        }
    }

    static BufferedImage scale(BufferedImage src, int w, int h)
    {
        BufferedImage img = new BufferedImage(w, h, BufferedImage.TYPE_INT_ARGB);
        int x, y;
        int ww = src.getWidth();
        int hh = src.getHeight();
        for (x = 0; x < w; x++) {
            for (y = 0; y < h; y++) {
                int col = src.getRGB(x * ww / w, y * hh / h);
                // For inverting colors if desired.
//                Color c = new Color(col, true);
//                c = new Color(255 - c.getRed(),
//                        255 - c.getGreen(),
//                        255 - c.getBlue());
//
//                img.setRGB(x, y, c.getRGB());
                img.setRGB(x, y, col);
            }
        }
        return img;
    }

